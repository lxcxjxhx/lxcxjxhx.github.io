function escapeTsString(str) {
  return str
    .replace(/\\/g, "\\\\")
    .replace(/'/g, "\\'")
    .replace(/\n/g, "\\n")
    .replace(/\r/g, "\\r");
}

function formatDate(rssDate) {
  try {
    const d = new Date(rssDate);
    return d.toISOString().split("T")[0];
  } catch {
    return rssDate;
  }
}

async function fetchRSS() {
  try {
    const res = await fetch(RSS_URL, {
      headers: {
        "User-Agent": "Mozilla/5.0 (Windows NT 10.0; Win64; x64) AppleWebKit/537.36",
        Accept: "application/rss+xml,application/xml,text/xml,*/*;q=0.9",
      },
    });
    if (!res.ok) {
      console.warn(`CSDN RSS returned ${res.status}, using fallback.`);
      return null;
    }
    return await res.text();
  } catch (err) {
    console.warn("CSDN RSS fetch failed:", err.message);
    return null;
  }
}

function parseRSS(xml) {
  const items = xml.match(/<item>[\s\S]*?<\/item>/g) || [];
  const articles = [];

  for (const item of items.slice(0, 20)) {
    const titleMatch = item.match(/<title>(?:<!\[CDATA\[)?([\s\S]*?)(?:\]\]>)?<\/title>/);
    const linkMatch = item.match(/<link>(.*?)<\/link>/);
    const dateMatch = item.match(/<pubDate>(.*?)<\/pubDate>/);
    const descMatch = item.match(/<description>(?:<!\[CDATA\[)?([\s\S]*?)(?:\]\]>)?<\/description>/);

    const link = linkMatch ? linkMatch[1].trim() : "";
    const idMatch = link.match(/\/details\/(\d+)/);
    const id = idMatch ? idMatch[1] : "";

    if (!id || !titleMatch) continue;

    articles.push({
      id,
      title: titleMatch[1].trim(),
      summary: (descMatch ? descMatch[1].trim() : "").substring(0, 200),
      url: link,
      publishedAt: formatDate(dateMatch ? dateMatch[1].trim() : ""),
      readCount: 0,
    });
  }

  return articles;
}

async function main() {
  const xml = await fetchRSS();
  const articles = xml ? parseRSS(xml) : [];

  const finalArticles = articles.length > 0 ? articles : FALLBACK_ARTICLES;

  const lines = [
    "// Auto-generated by scripts/fetch-csdn-data.mjs",
    "// Do not edit manually",
    "",
    "export interface CSDNArticle {",
    "  id: string;",
    "  title: string;",
    "  summary: string;",
    "  url: string;",
    "  publishedAt: string;",
    "  readCount: number;",
    "}",
    "",
    `export const csdnArticles: CSDNArticle[] = [`,
  ];

  for (const a of finalArticles) {
    lines.push(`  {`);
    lines.push(`    id: '${escapeTsString(a.id)}',`);
    lines.push(`    title: '${escapeTsString(a.title)}',`);
    lines.push(`    summary: '${escapeTsString(a.summary)}',`);
    lines.push(`    url: '${escapeTsString(a.url)}',`);
    lines.push(`    publishedAt: '${escapeTsString(a.publishedAt)}',`);
    lines.push(`    readCount: ${a.readCount},`);
    lines.push(`  },`);
  }

  lines.push(`];`);
  lines.push("");
  lines.push(`export const csdnStats = {`);
  lines.push(`  totalArticles: 1467,`);
  lines.push(`  columns: 12,`);
  lines.push(`};`);
  lines.push("");

  writeFileSync(OUTPUT, lines.join("\n"), "utf-8");
  console.log(`CSDN data written: ${finalArticles.length} articles`);
}

main();
