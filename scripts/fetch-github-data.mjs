// fetch-github-data.mjs
// Build-time script: fetches real GitHub data and generates src/data/github-data.ts

import fs from "fs";
import path from "path";
import { fileURLToPath } from "url";

const __dirname = path.dirname(fileURLToPath(import.meta.url));
const OUTPUT_FILE = path.join(__dirname, "..", "src", "data", "github-data.ts");
const USERNAME = "lxcxjxhx";

async function fetchJSON(url) {
  const res = await fetch(url, {
    headers: {
      "Accept": "application/vnd.github.v3+json",
      "User-Agent": "hos-blog-fetch-script",
    },
  });
  if (!res.ok) {
    console.error(`Failed to fetch ${url}: ${res.status} ${res.statusText}`);
    return null;
  }
  return res.json();
}

function camelCase(str) {
  return str.replace(/-([a-z])/g, (_, c) => c.toUpperCase());
}

async function main() {
  console.log(`Fetching GitHub data for ${USERNAME}...`);

  const [profile, allRepos, events] = await Promise.all([
    fetchJSON(`https://api.github.com/users/${USERNAME}`),
    fetchJSON(`https://api.github.com/users/${USERNAME}/repos?per_page=100&sort=updated&type=owner`),
    fetchJSON(`https://api.github.com/users/${USERNAME}/events/public?per_page=100`),
  ]);

  if (!profile) {
    console.error("Failed to fetch profile. Using fallback data.");
    process.exit(1);
  }

  // Filter original repos: exclude forks, the site repo, and profile README repo
  const repos = (allRepos || [])
    .filter((r) => !r.fork)
    .filter((r) => r.name !== "lxcxjxhx.github.io")
    .filter((r) => r.name !== "lxcxjxhx");

  // Transform profile
  const githubProfile = {
    login: profile.login,
    name: profile.name || profile.login,
    bio: profile.bio || "",
    avatarUrl: profile.avatar_url,
    htmlUrl: profile.html_url,
    publicRepos: profile.public_repos,
    followers: profile.followers,
    following: profile.following,
    createdAt: profile.created_at,
  };

  // Transform repos
  const githubRepos = repos.map((r) => ({
    id: r.id,
    name: r.name,
    fullName: r.full_name,
    description: r.description,
    htmlUrl: r.html_url,
    homepage: r.homepage,
    language: r.language,
    stargazersCount: r.stargazers_count,
    forksCount: r.forks_count,
    openIssuesCount: r.open_issues_count,
    topics: r.topics || [],
    pushedAt: r.pushed_at,
    createdAt: r.created_at,
    license: r.license?.spdx_id || null,
  }));

  // Transform events - keep original structure but camelCase keys
  const githubEvents = (events || []).map((e) => ({
    type: e.type,
    repo: e.repo?.name || "",
    createdAt: e.created_at,
    payload: e.payload ? {
      action: e.payload.action || null,
      ref: e.payload.ref || null,
      ref_type: e.payload.ref_type || null,
      pulls: e.payload.pull_request ? {
        number: e.payload.pull_request.number || null,
        title: e.payload.pull_request.title || "",
        html_url: e.payload.pull_request.html_url || "",
      } : undefined,
      issues: e.payload.issue ? {
        number: e.payload.issue.number || null,
        title: e.payload.issue.title || "",
        html_url: e.payload.issue.html_url || "",
      } : undefined,
      commits: e.payload.commits ? e.payload.commits.map((c) => ({
        message: c.message || "",
      })) : undefined,
      comment: e.payload.comment ? {
        body: e.payload.comment.body || "",
      } : undefined,
    } : {},
  }));

  console.log(`Profile: ${githubProfile.login}`);
  console.log(`Repos: ${githubRepos.length} (original, non-fork)`);
  console.log(`Events: ${githubEvents.length}`);

  // Generate TypeScript file
  const content = `// Auto-generated by scripts/fetch-github-data.mjs
// Do not edit manually - run "npm run fetch-data" to regenerate

export interface GitHubProfile {
  login: string;
  name: string;
  bio: string;
  avatarUrl: string;
  htmlUrl: string;
  publicRepos: number;
  followers: number;
  following: number;
  createdAt: string;
}

export interface GitHubRepo {
  id: number;
  name: string;
  fullName: string;
  description: string | null;
  htmlUrl: string;
  homepage: string | null;
  language: string | null;
  stargazersCount: number;
  forksCount: number;
  openIssuesCount: number;
  topics: string[];
  pushedAt: string;
  createdAt: string;
  license: string | null;
}

export interface GitHubEvent {
  type: string;
  repo: string;
  createdAt: string;
  payload: Record<string, any>;
}

export const githubProfile: GitHubProfile = ${JSON.stringify(githubProfile, null, 2)};

export const githubRepos: GitHubRepo[] = ${JSON.stringify(githubRepos, null, 2)};

export const githubEvents: GitHubEvent[] = ${JSON.stringify(githubEvents, null, 2)};
`;

  fs.writeFileSync(OUTPUT_FILE, content, "utf-8");
  console.log(`Generated ${OUTPUT_FILE}`);
}

main().catch((err) => {
  console.error(err);
  process.exit(1);
});
